@Composable
fun MovieGrid(movie: Movie) {
    LazyVerticalGrid(
        columns = GridCells.Fixed(2), // Sets the grid to have 2 columns
        verticalArrangement = Arrangement.Center,
        horizontalArrangement = Arrangement.Center,
        contentPadding = PaddingValues(16.dp),
    )
    {
        items(10) {
            MovieItem(movie : List<Movie>)
        }
    }
}
